function parseQuestions() {
    const lines = questionsText.split('\n');
    const questions = [];

    for (let line of lines) {
        line = line.trim();
        if (!line) continue;

        // 匹配题目序号和题干，格式如 "1、内容" 或 "101、内容"
        const match = line.match(/^(\d+)[、\.]\s*(.*)$/);
        if (!match) continue;

        const num = parseInt(match[1]);
        const content = match[2];

        const question = {
            id: num,
            text: '',
            options: [],
            answer: '',
            type: 'single'
        };

        // 答案与题干在同一行，格式：...答案：X
        const ansIdx = content.indexOf('答案：');
        let bodyText = (ansIdx !== -1) ? content.substring(0, ansIdx).trim() : content;

        if (ansIdx !== -1) {
            const ans = content.substring(ansIdx + 3).trim(); // "答案：" 长度为 3

            // 使用 includes 判断 '√'/'×'（Unicode 字符可能为 √/×）
            if (ans.includes('√') || ans.includes('×')) {
                question.type = 'judge';
                question.answer = ans === '√' ? '正确' : '错误';
            } else if (ans.length > 1) {
                question.type = 'multiple';
                question.answer = ans;
            } else {
                question.type = 'single';
                question.answer = ans;
            }
        }

        if (question.type !== 'judge') {
            // 提取选项，支持 "A. text" 和 "E优化"（无句点）两种格式
            // 前瞻：下一个选项是 "空格+大写字母"，后跟句点/顿号/中文字符
            const optPattern = /([A-Z])(?:[\.\、])?\s*(.+?)(?=\s+[A-Z](?:[\.\、]|[一-鿿])|答案：|$)/g;
            let optMatch;
            while ((optMatch = optPattern.exec(bodyText)) !== null) {
                question.options.push({
                    key: optMatch[1],
                    text: optMatch[2].trim()
                });
            }
            // 清理题干：取第一个选项前的部分
            question.text = bodyText.split(/\s+[A-Z][\.\、]?\s*/)[0].trim();
        } else {
            question.text = bodyText;
        }
        // 去掉题干末尾的（）或()
        question.text = question.text.replace(/[（(]\s*[）)]\s*$/, '').trim();

        questions.push(question);
    }
    return questions;
}

module.exports = async (db) => {
    // 创建表
    db.serialize(() => {
        // 题库表
        db.run(`CREATE TABLE IF NOT EXISTS questions (
            id INTEGER PRIMARY KEY,
            text TEXT,
            type TEXT,
            options TEXT,
            answer TEXT
        )`);
        
        // 用户答题记录表
        db.run(`CREATE TABLE IF NOT EXISTS user_records (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            serial_no INTEGER UNIQUE,
            openid TEXT UNIQUE,
            score INTEGER,
            answers TEXT,
            question_ids TEXT,
            submit_time TEXT
        )`);
        
        // 临时答题会话表（存储用户抽中的题目）
        db.run(`CREATE TABLE IF NOT EXISTS temp_sessions (
            openid TEXT PRIMARY KEY,
            questions TEXT,
            created_at TEXT
        )`);
        
        // 检查题库是否为空
        db.get("SELECT COUNT(*) as cnt FROM questions", (err, row) => {
            if (err) {
                console.error(err);
                return;
            }
            if (row.cnt === 0) {
                console.log("正在初始化题库...");
                const questions = parseQuestions();
                const stmt = db.prepare("INSERT INTO questions (id, text, type, options, answer) VALUES (?, ?, ?, ?, ?)");
                for (let q of questions) {
                    stmt.run(q.id, q.text, q.type, JSON.stringify(q.options), q.answer);
                }
                stmt.finalize();
                console.log(`题库初始化完成，共 ${questions.length} 道题`);
            }
        });
    });
};